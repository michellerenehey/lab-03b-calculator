// create a base for the function ideas

export function add(num1, num2) {
    return Number(num1) + Number(num2);

}; 

export function sub(num1, num2) {
    return Number(num1) - Number(num2);

}; 

export function mult(num1, num2) {
    return Number(num1) * Number(num2);
}; 

export function div(num1, num2) {
    return Number(num1) / Number(num2);

}; 